#include <string.h>

char	*ft_strncpy(char *str, const char *str3, size_t l)
{
	size_t		i;

	i = 0;
	while (str3[i] != '\0' && i < l)
	{
		str[i] = str3[i];
		i++;
	}
	if (i != (l + 1))
	{
		while (i < l)
		{
			str[i] = '\0';
			i++;
		}
	}
	return (str);
}
