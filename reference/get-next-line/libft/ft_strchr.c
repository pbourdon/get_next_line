#include <stdlib.h>
char	*ft_strchr(const char *str, int l)
{
	char	c;

	c = (char)l;
	while (*str++ != c)
	{
		if (*str == '\0' && c != '\0')
			return (NULL);
	}
	str--;
	return ((char*)str);
}
